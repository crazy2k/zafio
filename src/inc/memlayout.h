#ifndef __MEMLAYOUT_H__

#define __MEMLAYOUT_H__

#define KERNEL_OFFSET 0xC0000000
#define KPHADDR(dir) ((dir) - KERNEL_OFFSET)

#define KERNEL_VIRT_ADDR 0xC0100000
#define KERNEL_PHYS_ADDR KPHADDR(KERNEL_VIRT_ADDR)

#define KERNEL_PAGE_TABLE (KERNEL_VIRT_ADDR + 0x00100000)        // kernel + 1MB
#define KERNEL_PAGE_DIRECTORY (KERNEL_PAGE_TABLE + 0x00001000)   // kernel + 1MB + 4K
#define KERNEL_STACK (KERNEL_PAGE_DIRECTORY + 0x00001000)        // kernel + 1MB + 8K

#define VIDEO_MEMORY 0x000B8000

#endif
